/*	
 * That is simplify loop for normal FOR loop
 * 
 * Syntax:
 * 			for(int var_name: array name)
 * 					//body of the code
 * 
 */



public class HanceForLoop {

	public static void main(String args[])
	{
	}
	
	public void hanceFor(int arr[])
	{
		for (int k : arr)
			System.out.println(k);
	}
	
}
